package hotel.domain.rest;

public class Data {
	
	private String text;
	private String roomName;
	
	public Data(){}
	
	public String getRoomName() {
		return roomName;
	}

	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	};
	
}
